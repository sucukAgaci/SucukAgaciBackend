package com.sucukagaci;

import java.io.Serializable;
import java.util.Objects;

/**
 *
 * @author umurgogebakan
 */
public class Destination implements Serializable {
    private String name;
    private double longtitude;
    private double latitude;

    public Destination() {
    }

    public Destination(String name, double longtitude, double latitude) {
        this.name = name;
        this.longtitude = longtitude;
        this.latitude = latitude;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getLongtitude() {
        return longtitude;
    }

    public void setLongtitude(double longtitude) {
        this.longtitude = longtitude;
    }

    public double getLatitude() {
        return latitude;
    }

    public void setLatitude(double latitude) {
        this.latitude = latitude;
    }

    @Override
    public String toString() {
        return "Destination{" + "name=" + name + ", longtitude=" + longtitude + ", latitude=" + latitude + '}';
    }

    @Override
    public int hashCode() {
        int hash = 5;
        hash = 97 * hash + Objects.hashCode(this.name);
        hash = 97 * hash + (int) (Double.doubleToLongBits(this.longtitude) ^ (Double.doubleToLongBits(this.longtitude) >>> 32));
        hash = 97 * hash + (int) (Double.doubleToLongBits(this.latitude) ^ (Double.doubleToLongBits(this.latitude) >>> 32));
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Destination other = (Destination) obj;
        if (Double.doubleToLongBits(this.longtitude) != Double.doubleToLongBits(other.longtitude)) {
            return false;
        }
        if (Double.doubleToLongBits(this.latitude) != Double.doubleToLongBits(other.latitude)) {
            return false;
        }
        if (!Objects.equals(this.name, other.name)) {
            return false;
        }
        return true;
    }
    
    
}
